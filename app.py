# Lista com 15 nomes de cidades
cidades = [
    "Rio de Janeiro", "São Paulo", "Belo Horizonte", "Brasília", "Curitiba",
    "Porto Alegre", "Salvador", "Fortaleza", "Manaus", "Recife",
    "Belém", "Goiânia", "Campinas", "Florianópolis", "Natal"
]

# Ordenando a lista em ordem alfabética
cidades_ordenadas = sorted(cidades)

# Exibindo as cidades em ordem alfabética
print("Cidades em ordem alfabética:")
for cidade in cidades_ordenadas:
    print(cidade)
